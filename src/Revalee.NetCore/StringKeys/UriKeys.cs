namespace Revalee.NetCore.StringKeys
{
    internal class UriKeys
    {
        /// <summary>
        /// Specifies that the URI is accessed through the Hypertext Transfer Protocol (HTTP)
        /// </summary>
        internal const string UriSchemeHttp = "http";

        /// <summary>
        /// Specifies that the URI is accessed through the Secure Hypertext Transfer Protocol (HTTPS)
        /// </summary>
        internal const string UriSchemeHttps = "https";

        /// <summary>
        /// Specifies the characters that separate the communication protocol scheme from the address
        /// portion of the URI
        /// </summary>
        internal const string SchemeDelimiter = "://";
    }
}
